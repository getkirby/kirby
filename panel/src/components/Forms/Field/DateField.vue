<template>
  <k-field :input="_uid" v-bind="$props" class="k-date-field">
    <k-input
      ref="input"
      :id="_uid"
      :type="inputType"
      :value="value"
      v-bind="$props"
      theme="field"
      v-on="listeners"
    >
      <template #icon>
        <k-dropdown>
          <k-button
            :icon="icon"
            :tooltip="$t('date.select')"
            class="k-input-icon-button"
            tabindex="-1"
            @click="$refs.dropdown.toggle()"
          />
          <k-dropdown-content ref="dropdown" align="right">
            <k-calendar
              :value="datetime"
              :min="min"
              :max="max"
              @input="onSelect"
            />
          </k-dropdown-content>
        </k-dropdown>
      </template>

    </k-input>
  </k-field>
</template>

<script>
import Field from "../Field.vue";
import Input from "../Input.vue";
import DateTimeInput from "../Input/DateTimeInput.vue";

export default {
  inheritAttrs: false,
  props: {
    ...Field.props,
    ...Input.props,
    ...DateTimeInput.props,
    icon: {
      type: String,
      default: "calendar"
    }
  },
  data() {
    return {
      datetime: this.value
    };
  },
  computed: {
    inputType() {
      return this.time === false ? "date" : "datetime";
    },
    listeners() {
      return {
        ...this.$listeners,
        blur: this.onBlur,
        enter: this.onSelect,
        focus: this.onFocus,
        input: this.onInput
      };
    }
  },
  watch: {
    value(value) {
      this.datetime = value;
    }
  },
  methods: {
    focus() {
      this.$refs.input.focus();
    },
    onBlur(value) {
      this.$emit("input", value);
    },
    onFocus() {
      this.$refs.dropdown.open();
    },
    onInput(value) {
      this.datetime = value;
    },
    onSelect(value) {
      this.onBlur(value);
      this.$refs.dropdown.close();
    }
  }
}
</script>
