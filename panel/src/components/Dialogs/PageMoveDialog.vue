<template>
	<k-dialog
		ref="dialog"
		:submit-button="{
			icon: 'parent',
			text: $t('move')
		}"
		class="k-page-move-dialog"
		size="medium"
		v-bind="$props"
		@cancel="$emit('cancel')"
		@submit="$emit('submit', value)"
	>
		<k-headline>{{ $t("page.move") }}</k-headline>
		<div class="k-page-move-parent" tabindex="0" data-autofocus>
			<k-page-tree
				:current="value.parent"
				:move="value.move"
				identifier="id"
				@select="select"
			/>
		</div>
	</k-dialog>
</template>

<script>
import Dialog from "@/mixins/dialog.js";

/**
 * @since 4.0.0
 */
export default {
	mixins: [Dialog],
	props: {
		value: {
			default() {
				return {};
			},
			type: Object
		}
	},
	emits: ["cancel", "input", "submit"],
	methods: {
		select(page) {
			this.$emit("input", { ...this.value, parent: page.id });
		}
	}
};
</script>

